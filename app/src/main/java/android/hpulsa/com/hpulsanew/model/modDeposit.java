public class modDeposit {
    private String id;
    private String bank;
    private String tglDeposit;
    private String sttsDeposit;
    private String jmlDeposit;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getBank() {
        return bank;
    }

    public void setBank(String bank) {
        this.bank = bank;
    }

    public String getTglDeposit() {
        return tglDeposit;
    }

    public void setTglDeposit(String tglDeposit) {
        this.tglDeposit = tglDeposit;
    }

    public String getSttsDeposit() {
        return sttsDeposit;
    }

    public void setSttsDeposit(String sttsDeposit) {
        this.sttsDeposit = sttsDeposit;
    }

    public String getJmlDeposit() {
        return jmlDeposit;
    }

    public void setJmlDeposit(String jmlDeposit) {
        this.jmlDeposit = jmlDeposit;
    }
}
