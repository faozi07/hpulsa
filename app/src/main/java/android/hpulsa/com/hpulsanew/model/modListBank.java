public class modListBank {
    private int id;
    private String namaBank;
    private String keyBank;
    private String namaRek;
    private String noRek;


    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNamaBank() {
        return namaBank;
    }

    public void setNamaBank(String namaBank) {
        this.namaBank = namaBank;
    }

    public String getKeyBank() {
        return keyBank;
    }

    public void setKeyBank(String keyBank) {
        this.keyBank = keyBank;
    }

    public String getNamaRek() {
        return namaRek;
    }

    public void setNamaRek(String namaRek) {
        this.namaRek = namaRek;
    }

    public String getNoRek() {
        return noRek;
    }

    public void setNoRek(String noRek) {
        this.noRek = noRek;
    }
}
